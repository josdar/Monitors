package domain;

/**
 *
 * @author joset
 */
public class Servidor {
    String ip;
    String nombre;

    public Servidor() {
    }

    public Servidor(String ip, String nombre) {
        this.ip = ip;
        this.nombre = nombre;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String toString() {
        return ip +"-"+ nombre ;
    }
    
    
}
